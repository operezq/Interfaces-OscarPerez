    private void btnTrianguloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrianguloActionPerformed
        double base, altura;
        base = Double.parseDouble(JOptionPane.showInputDialog("Ingrese el valor de la base del triángulo: "));
        altura = Double.parseDouble(JOptionPane.showInputDialog("Ingrese el valor de la altura del triángulo: "));
        areaTriangulo.setText(String.valueOf((triangulo.calcularArea(base,altura))*100.0/100.0));
        perimetroTriangulo.setText(String.valueOf((triangulo.calcularPerimetro(base,altura)*100.0/100.0)));
        Hipotenusa.setText(String.valueOf(triangulo.calcularHipotenusa(base,altura)));
        TipoTriangulo.setText(String.valueOf(triangulo.determinarTipoTriangulo(base,altura)));
        // TODO add your handling code here:
    }//GEN-LAST:event_btnTrianguloActionPerformed

