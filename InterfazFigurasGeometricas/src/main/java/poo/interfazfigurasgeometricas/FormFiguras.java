    private void btnCirculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCirculoActionPerformed
           int radio;
           radio = Integer.parseInt(JOptionPane.showInputDialog(null,"Ingrese el radio del círculo"));
           areaCirculo.setText(String.valueOf((circulo.calcularArea(radio)*100.0)/100.0));
           perimetroCirculo.setText(String.valueOf((circulo.calcularPerimetro(radio)*100.0/100.0)));
    }//GEN-LAST:event_btnCirculoActionPerformed

